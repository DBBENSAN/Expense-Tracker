const { Model, DataTypes } = require('sequelize')
const sequelize = require('../config/connection');
const Quest = require('./Quest')
const User = require('./User')

class QuestUser extends Model {}


QuestUser.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
    },
    user_id: {
      type: DataTypes.INTEGER,
      references: {
        model: 'user',
        key: 'id',
      },
    },
    quest_id: {
      type: DataTypes.INTEGER,
      references: {
        model: 'user',
        key: 'id',
      },
    },
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'questUser',
  }
);

module.exports = QuestUser;